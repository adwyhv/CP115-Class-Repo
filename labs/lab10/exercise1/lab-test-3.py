monthly_usage = input()
amount_bill = 0
#No discount if monthly_usage >=50
if monthly_usage < 50 :
    discount = 0
#get 5% discount for monthly usage less or equal to 100
elif monthly_usage <= 100:
    discount = 0.05
else:
    discount = 0.2
#discount before
total_discount = monthly_usage * discount
amount_bill = total_discount - discount

print(monthly_usage)
print(amount_bill)

